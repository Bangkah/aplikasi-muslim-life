    public class Catatan {
    String tanggal;
    String waktu;
    String isi;

    public Catatan(String tanggal, String waktu, String isi) {
        this.tanggal = tanggal;
        this.waktu = waktu;
        this.isi = isi;
    }

    @Override
    public String toString() {
        return tanggal + " " + waktu + " - " + isi;
    }
}


